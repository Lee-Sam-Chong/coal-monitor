    #   GET easyquery.htm?m=getTree&id=A0&dbcode=fsyd&wdcode=zb
    # 然后递归查找 A02 (工业) → A0201 (产品产量) → 找到"原煤产量"
    #
    # 常见候选值（需要你选择一个并验证）：
    #   A02010101  ~  工业产品产量下的细分指标
    #   A020802   ~  煤炭开采和洗选业相关
    #
    # TODO: 请使用下面的 discover_indicators() 函数找到正确的编码
